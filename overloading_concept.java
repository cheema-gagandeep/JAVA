
    public class overloading_concept
 {
     void sum(int x,int y)
     {
         System.out.println("Sum of two integer number"+(x+y));
     }
     
     void sum(double x,double y)
     {
         System.out.println("Sum of two double number"+(x+y));
     }
     
     void sum(char x,char y)
     {
         System.out.println("Sum of two char number"+(x+y));
     }
}
    

