public class ArrayAccessDemo {
    public static void main(String []  arg)
    {
        int[] num=new int[5];
        num[0]=5;//Assigning value 5 to element at index 0
        num[1]=15;
        num[2]=25;
        num[3]=30;
        num[4]=35;

        System.out.println("Array Eement num[0]="+num[0]);
        System.out.println("Array Eement num[1]="+num[1]);
        System.out.println("Array Eement num[2]="+num[2]);
        System.out.println("Array Eement num[3]="+num[3]);
        System.out.println("Array Eement num[4]="+num[4]);


    }
    
}
