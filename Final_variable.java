/* The final variable also called constant variable.
It is a variable ith a value that cannot be modified 
during the execution of the program */
public class Final_variable {
    public static void main(String[] arg)
    {
        int r=10;
        final double PI=3.11592;
        double area;
        area=PI*r*r;
        System.out.println("Area of circle is -->"+area);
    }
}
